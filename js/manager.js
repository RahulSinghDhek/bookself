function updateBackgroundColors(tabs,tabid){
	for (i = 0 ; i< tabs.length;i++){
		if(tabs[i]!==tabid){
		  document.getElementById(tabs[i]).style.backgroundColor = "white";
		}else{
		  document.getElementById(tabs[i]).style.backgroundColor = "#c0dde0";
		}
	} 
}
function updateTabBackgroundColors(tabid,isSubTab){
	var tabs= ["currenttab","justfinishedtab","upcomingtab","completedtab","unfinishedtab","mustreadtab","2021tab"];
	var subTabs = ["alltab","financetab","fictiontab","nonfictiontab","selfimprovementtab"]; 
	if(isSubTab){
		updateBackgroundColors(subTabs,tabid);
		return;
	}
	updateBackgroundColors(tabs,tabid);
	return;
}


function bookShelf(username, password){
	var x = document.getElementsByClassName("tabName");
	for (i = 0; i < x.length; i++) {
		x[i].style.display = "none";  
	}
	document.getElementById("current").style.display = "block"; 
	hideCompletedReads();
}

function updateBookShelf(){
	var imageSource = null;
	var bookUrl = null;
	var bookTags = null;
	var mainDiv = "";
	for (var bookName in bookShelf) {
		imageSource = bookShelf[bookName]["imageSource"]
		bookTags = bookShelf[bookName]["tags"]
		bookUrl = bookShelf[bookName]["url"]
		for ( i=0; i< bookTags.length;i++){
			mainDiv += '<div class="w3-third w3-container w3-margin-bottom"><a href="'+bookUrl+'" ><img src="'+imageSource+'" style="width:250px;height: 350px;" class="w3-hover-opacity" >'
			mainDiv += '<div class="w3-container w3-white"></div></a></div>'
			mainDiv += '</div>'
			document.getElementById(bookTags[i]).insertAdjacentHTML('beforeend', mainDiv)
			mainDiv="";
		}	
	}
}

